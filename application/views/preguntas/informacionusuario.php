    <h5> VISTA  2  (Preguntas  frecuentes  antes  de  comenzar)</h5>
</div>
<br>
¿Qué  es  el  PESV?  
<br>
<p>El  PESV  (plan  estratégico  de  seguridad  vial)  es  el  instrumento  de  planificación  que  consignado  en  un  documento  contiene  las   acciones,  mecanismos,  estrategias  y  medidas  que  deberán  adoptar  las  diferentes  entidades,  organizaciones  o  empresas  del  sector  público  y  privado  existentes  en  Colombia.      

Dichas  acciones  están  encaminadas  a  alcanzar  la  seguridad  vial  como  algo  inherente  al  ser  humano  y  así   reducir  la  accidentalidad  vial  de  los  integrantes  de  las  organizaciones  mencionadas  y  de  no  ser  posible  evitar,  o  disminuir  los  efectos  que  puedan  generar  los  accidentes  de  tránsito.  </p>

<p>¿Qué  tengo  yo  que  ver  con  esto? </p> 
<br>
<p>Todas  las  personas  que  vivimos  en  el  planeta  tierra,  salimos  de  nuestras  casas  y  utilizamos  las  vías  públicas  para  hacer  nuestras  actividades  diarias.  En  la  vía  podemos  ser  peatones,  pasajeros  o  conductores.  Cualquiera  de  estos  3  roles  en  la  vía  pueden  tener  riesgos  de  accidentes  o  incidentes  de  tránsito.  Y  usted,  pasa  por  uno  o  varios  de  estos  roles,  mientras  va  de  su  casa  al  trabajo  y  del  trabajo  a  su  casa  (desplazamiento  que  recibe  el  nombre  de  in  itinere).  También  puede  ser  pasajero,  peatón  o  conductor  mientras  realiza  tareas  en  misión,  es  decir  dentro  de  sus  labores  de  trabajo.  Por  eso  es  importante    que  usted  y  las  personas  que  lo  rodean,  forme  hábitos,  comportamientos  y    conductas  que  ayuden  a  reducir  accidentes  e  incidentes  

de  tránsito.    Recuerde  que  la  prioridad  sobre  la  vía  la  tiene  LA  VIDA.</p>  

