<p>¿Qué  es  el  PESV?  </p>

<p>El  PESV  (plan  estratégico  de  seguridad  vial)  es  el  instrumento  de  planificación  que  consignado  en  un  documento  contiene  las   acciones,  mecanismos,  estrategias  y  medidas  que  deberán  adoptar  las  diferentes  entidades,  organizaciones  o  empresas  del  sector  público  y  privado  existentes  en  Colombia.   </p>   

<p>Dichas  acciones  están  encaminadas  a  alcanzar  la  seguridad  vial  como  algo  inherente  al  ser  humano  y  así   reducir  la  accidentalidad  vial  de  los  integrantes  de  las  organizaciones  mencionadas  y  de  no  ser  posible  evitar,  o  disminuir  los  efectos  que  puedan  generar  los  accidentes  de  tránsito.  </p>

<p>¿Para  qué  sirve  el  plan  estratégico  de  seguridad  vial?  <p>

<p>La  finalidad  del  Plan  Estratégico  de  Seguridad  Vial,  es  definir  los  objetivos  y  las  acciones  o  intervenciones  concretas  que  se  deben  llevar  a  cabo  para  alcanzar  los  propósitos  en  materia  de  prevención  de  los  accidentes  de  tránsito,  facilitando  la  gestión  de  la  organización  al  definir  las  áreas  involucradas,  los  responsables  y  los  mecanismos  de  evaluación  y  seguimiento  en  función  del  cumplimiento  de  las  actuaciones  definidas  .  </p>

<p>¿Cuáles  son  los  objetivos  de  un  plan  estratégico  de  seguridad  vial?  </p>

<p>Con  los  objetivos  del  plan  estratégico  de  seguridad  vial  se  traza  un  mapa  que  señala  resultados  que  la  organización  se  ha  propuesto  alcanzar  en  materia  de  seguridad  vial. </p> 

<p><b>¿Qué  es  la  seguridad  vial? </b></p> 

<p>Se  refiere  al  conjunto  de  acciones,  mecanismos,  estrategias  y  medidas  orientadas  a  la  prevención  de  accidentes  de  tránsito,  o  a  anular  o  disminuir  los  efectos  de  los  mismos,  con  el  objetivo  de  proteger  la  vida  de  los  usuarios  de  las  vías. </p> 

<p><b>¿Qué  es  la  seguridad  activa? </b></p>   

<p>Se  refiere  al  conjunto  de  mecanismos  o  dispositivos  del  vehículo  automotor  destinados  a  proporcionar  una  mayor  eficacia  en  la  estabilidad  y  control  del  vehículo  en  marcha  para  disminuir  el  riesgo  de  que  se  produzca  un  accidente  de  tránsito  .  </p>

<p><b>¿Qué  es  seguridad  pasiva?</b></p>    

<p>Son  los  elementos  del  vehículo  automotor  que  reducen  los  daños  que  se  pueden  producir  cuando  un  accidente  de  tránsito  es  inevitable  y  ayudan  a  minimizar  los  posibles  daños  a  los  ocupantes  del  vehículo. </p> 

<p><b>¿Qué  es  un  accidente  de  tránsito? </b></p>    

<p>Es  un  evento  generalmente  involuntario,  generado  al  menos  por  un  vehículo  en  movimiento,  que  causa  daños  a  personas  y  bienes  involucrados  en  él,  e  igualmente  afecta  la  normal  circulación  de  los  vehículos  que  se  movilizan  por  la  vía  o  vías  comprendidas  en  el  lugar  o  dentro  de  la  zona  de  influencia  del  hecho   (CNTT,  

2002).</p>    

<p><b>¿Qué  es  un  accidente  de  trabajo?</b></p>   

<p>Todo  suceso  repentino  que  sobrevenga  con  causa  u  ocasión  del  trabajo  y  que  produzca  en  el  trabajador  una  lesión  orgánica,  una  perturbación  funcional  o  psiquica,  una  invalidez  o  la  muerte.  Así  como  el  que  se  produce  durante  la  ejecución  de  ordenes  del  empleador  o  contratante,  durante  la  ejecución  de  una  labor  bajo  su  autoridad,  aún  por  fuera  del  lugar  y  horas  de  trabajo;  igualmente  el  que  se  produzca  durante  el  traslado  de  los  trabajadores  o  contratistas  desde  su  residencia  a  los  lugares  de  trabajo  y  viceversa,  cuando  el  transporte  lo  suministre  el  empleador.(Ley  1562  de  2012}.  </p>

<p><b>¿Qué  es  un  riesgo?  </b></p>     

<p>Es  la  evaluación  de  las  consecuencias  de  un  peligro,  expresada  en  términos  de  probabilidad  y  severidad,  tomando  como  referencia  la  peor  condición  previsible.  </p>

<p><b>¿Qué  es  una  amenaza?  </b></p>     

<p>Se  define  como  la  probabilidad  de  ocurrencia  de  un  suceso  potencialmente  desastroso,  durante  cierto  período  de  tiempo  en  un  sitio  dado. </p> 

<p><b>¿Qué  es  vulnerabilidad?  </b></p>     

<p>Probabilidad  de  afectación,  puede  decirse  también,  de  la  susceptibilidad  de  ser  afectado  por  una  amenaza  y  su  capacidad  de  sobreponerse.</p>  

<p><b>¿Quién  es  un  peatón?  </b></p>

<p>Persona  que  transita  a  pie  por  una  vía  (CNTI,  2002).</p>  

<p><b>¿Quién  es  un  pasajero?</b></p>    

<p>Persona  distinta  del  conductor  que  se  transporta  en  un  vehículo  público  (CNTI,  

2002).  </p> 

<p><b>¿Quién  es  un  conductor? </b></p>   

<p>Es  la  persona  habilitada  y  capacitada  técnica  y  teóricamente  para  operar  un  vehículo  (CNTI,  2002).  </p>

<p><b>¿Qué  es  el  SOAT? </b></p>    

<p>Seguro  Obligatorio  de  Accidentes  de  Tránsito  -­‐  SOAT,  el  cual  ampara  los  daños  corporales  que  se  causen  a  las  personas  en  accidentes  de  tránsito  e  indemniza  a  los  beneficiarios  o  las  víctimas  por  muerte  o  incapacidad  médica  según  el  caso.  </p>

 <p><b> ¿Qué  es  la  ARL? </b></p>    

<p>La  Administradora  de  Riesgos  Laborales  (ARL)  es  una  entidad  aseguradora  de  vida,  encargada  de  afiliar  a  los  empleados  al  sistema  general  de  riesgos  laborales  y  de  prevenir,  proteger  y  atender  a  los  trabajadores  contra  todo  evento  riesgoso  que  puede  haber  en  un  ambiente  laboral.  </p>

 <p><b> ¿Qué  es  el  HSEQ? </b></p> 

<p>Es  un  sistema  de  gestión  por  medio  de  cual  se  garantiza  el  manejo  responsable  de  todas  las  actividades  de  la  organización,  promoviendo  y  mejorando  la  salud  del  personal,  garantizando  un  trabajo  sin  riesgo  de  lesiones  a  éste  o  a  los  demás,  promoviendo  la  protección  del  medio  ambiente  y  asegurando  la  calidad  en  los  procesos. </p>  

 <p><b> ¿Qué  es  una  estrategia? </b></p>    

<p>Comprende  las  principales  orientaciones  y  acciones  encaminadas  a  lograr  los  objetivos  de  un  plan.  En  un  proceso  regulable,  conjunto  de  las  reglas  que  aseguran  una  decisión  óptima  en  cada  momento.  </p>

  <p><b> ¿Qué  es  la  vision?   </b></p> 

<p>Es  un  elemento  de  la  planeación  estratégica  que  enuncia  un  estado  futuro  de  lo  que  desea  alcanzar  una  organización  en  un  tiempo  determinado,  expresado  de  manera  realista  y  positiva  en  términos  de  objetivos. </p> 

  <p><b> ¿Qué  es  un  Plan  de  acción?   </b></p> 

<p>Corresponde  a  un  documento  que  reúne  el  conjunto  de  actividades  específicas,  los  recursos  y  los  plazos  necesarios  para  alcanzar  objetivos  de  un  proyecto,  así  como  las  orientaciones  sobre  la  forma  de  realizar,  supervisar  y  evaluar  las  actividades.  </p>  

<p><b> ¿Qué  es  una  entidad? </b></p>    

<p>Colectividad  considerada  como  unidad.  Especialmente,  cualquier  corporación,  compañía,  institución,  etc"  tomada  como  persona  juridical.  </p>

  <p><b> ¿Qué  es  una  Organización?   </b></p> 

<p>Asociación  de  personas  regulada  por  un  conjunto  de  normas  en  función  de  

determinados  fines.  </p>

<p><b>¿Qué  es  una  Empresa?  </b></p>   

<p>La  empresa  es  la  unidad  económico-­‐social  en  la  que  el  capital,  el  trabajo  y  la  dirección  se  coordinan  para  realizar  una  producción  socialmente  útil,  de  acuerdo  con  las  exigencias  del  bien  común.  Los  elementos  necesarios  para  formar  una  empresa  son:  capital,  trabajo  y  recursos  materiales. </p> 

<p><b>¿Qué  es  un  Vehículo? </b></p>  

<p>Todo  aparato  montado  sobre  ruedas  que  permite  el  transporte  de  personas,  animales  o  cosas  de  un  punto  a  otro  por  vía  terrestre  pública  o  privada  abierta  al  público  (CNIT.  2002).  </p>

<p><b>¿Qué  es  un  Vehículo  de  tracción  animal?   </b></p>  

<p>Vehículo  no  motorizado  halado  o  movido  por  un  animal  (CNIT.  2002).  </p>

<p><b>¿Qué  es  un  Vehículo  no  automotor?  </b></p> 

<p>Vehículo  que  se  desplaza  por  el  esfuerzo  de  su  conductor.  </p>

<p><b>¿Qué  es  el  CSV? </b></p> 

<p>Es  el  equipo  de  trabajo  definido  por  la  alta  dirección,  encargado  de  articular  los  planes  entre  todos  los  involucrados  y    cuyo  objetivo  será  plantear,  diseñar,  implementar  y  medir  las  acciones  que  permitan  generar  conciencia  entre  el  personal  y  lograr  objetivos  a  favor  de  la  seguridad  vial  en  la  empresa  y  la  vida  cotidiana  de  sus  integrantes.  <p>

